package InvoicingPackage;

public class InvoicingSystem {
	
	private String invoicingDate ;
	private int totalAmount ;
	private int totalAmountPaid ;
	
	
	
	
	
	
	
	public String getInvoicingDate() {
		return invoicingDate;
	}
	public void setInvoicingDate(String invoicingDate) {
		this.invoicingDate = invoicingDate;
	}
	public int getTotalAmount() {
		return totalAmount;
	}
	public void setTotalAmount(int totalAmount) {
		this.totalAmount = totalAmount;
	}
	public int getTotalAmountPaid() {
		return totalAmountPaid;
	}
	public void setTotalAmountPaid(int totalAmountPaid) {
		this.totalAmountPaid = totalAmountPaid;
	}
	

}
